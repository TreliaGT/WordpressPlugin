<h2>Shortcodes for Advance Custom fields</h2>
<hr>
<h3>[Gallery]</h3>
<p>Adds using the gallery custom field and also must be named gallery. This displays the content of the gallery</p>
<ul> 
    <li>link = true <br> Displays a link to the image</li>
    <li>overlay = true <br> displays a overlay </li>
    <li>caption = true <br> display the caption of the image </li>
</ul>
<h3>[repeaterfileList]</h3>
<p>Displays a list of files, file field must be named file</p>
<ul>
    <li> repeater = 'name' Must </li>
    <li>title = '<tag>Name</tag>' if want title</li>
</ul>
<h3>[ListPosts]</h3>
<p>Displays a list of custom posts</p>
<ul>
    <li> posttype = '[post type]' Must</li>
    <li> title = 'true'</li>
    <li>category = '[Name]' Adds name if title is true </li>
</ul>

